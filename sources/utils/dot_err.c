#include "../../includes/minirt.h"

bool	dot_err(char *s, int i)
{
	int	j;
	int	k;

	j = 0;
	k = 0;
	while (s[j])
	{
		if (s[j] == '.')
			k++;
		j++;
	}
	if (k > i)
		return (true);
	return (false);
}
