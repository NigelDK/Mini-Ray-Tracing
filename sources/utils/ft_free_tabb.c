#include "../../includes/minirt.h"

void	ft_free_tabb(char ***tab)
{
	int	i;

	i = 0;
	while (tab[0][i])
	{
		free(tab[0][i]);
		tab[0][i] = NULL;
		i++;
	}
	free(tab[0]);
	tab[0] = NULL;
}
